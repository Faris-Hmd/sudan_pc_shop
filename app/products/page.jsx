import Link from "next/link";
import SearchForm from "../comp/SearchForm";
import { getProducts } from "../data/products";
import Image from "next/image";
export const metadata = {
  title: "SPS | products Sesrch",
  description: "Generated by create next app",
};

export default async function Home({ searchParams }) {
  const { search_word } = await searchParams;
  let products = [];
  if (search_word) {
    products = await getProducts(search_word, 6);
  }

  return (
    <>
      <SearchForm />
      <div className="list mt-1">
        {products && products?.length > 0 ? (
          products.map((row) => (
            <div className="product_card" key={row.id}>
              <Link href={`products/${row.id}`} className="relative">
                <div className="h-30 w-full ms-auto relative bg-amber-300">
                  <Image
                    fill
                    src={row.p_imgs[0].url}
                    alt="Product Image"
                    // className="ms-auto"
                  />
                </div>
              </Link>
              <div className="name">{row.p_name}</div>
              <div className="wrapper">
                <span className="cost">{row.p_cost}</span>
                <span className="category"> | {row.p_cat}</span>
              </div>
            </div>
          ))
        ) : (
          <div className="no_product">No products available</div>
        )}
      </div>
    </>
  );
}
