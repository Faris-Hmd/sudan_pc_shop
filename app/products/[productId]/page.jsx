export const metadata = {
  title: `SPS | product detail`,
  description: "Generated by create next app",
};
import { getDoc, doc } from "firebase/firestore";
import { db } from "../../db/firebase";
import ProductImgCarousel from "@/app/comp/carousel";
import { getProductsIds } from "../../actions/product_ids";
import CarouselDial from "../../comp/CarouselDial";
import WishlistBtn from "../../comp/WishlistBtn";
import { auth } from "@/auth";
import { log } from "console";

export default async function ProductsDetails({ params }) {
  const sess = await auth();
  const { productId } = await params;
  const docRef = doc(db, "productsTest", productId);
  const docsnapshot = await getDoc(docRef);
  const product = docsnapshot.exists() ? docsnapshot.data() : {};
  let isWish = false;
  if (sess) {
    const _docRef = doc(db, "users", sess.user.email, "whish", productId);
    const _docsnapshot = await getDoc(_docRef);
    isWish = _docsnapshot.exists();
  }

  log(isWish);
  return (
    <div className="product_d_card object-cover">
      <ProductImgCarousel
        imgH={"h-80"}
        imgs={product.p_imgs}
        imgFill={"object-cover"}
      />
      <CarouselDial imgs={product.p_imgs} />
      <div className="p-2">
        <div className="product_d_name">{product.p_name}</div>
        <div className="wrapper">
          <span className="text-green-700 text-xs">{product.p_cost} SDG</span>
          <span className="category text-xs"> | {product.p_cat}</span>
        </div>
        <div className="product_d_details">
          Lorem ipsum, dolor sit amet consectetur adipisicing elit. Sequi,
          similique dolorem, delectus fugit optio provident omnis quia dolorum
          labore magni nostrum quae amet, in beatae ut ab voluptatibus nihil
          quam!
        </div>
        <div className="flex gap-2">
          <WishlistBtn productId={productId} isWish={isWish} />
          <button className="w-full text-white rounded p-2 bg-green-600">
            Pay Now
          </button>
        </div>
      </div>
    </div>
  );
}
